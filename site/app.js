// Mirror of the pipeline's dedup key, so cards can find their translation.
function keyFor(r) {
  const product = String(r.product || "").toLowerCase().replace(/[^a-z0-9]+/g, "");
  return `${String(r.company || "").trim().toLowerCase()}|${product}|${r.type || ""}`;
}

function isNew(dateStr) {
  if (!dateStr) return false;
  const days = (Date.now() - new Date(dateStr + "T00:00:00").getTime()) / 86400000;
  return days >= 0 && days <= 7; // announced within the last week
}

// Human "2 days ago" / "3 months ago" — the freshness cue the stats sites use.
function relativeDate(dateStr) {
  if (!dateStr) return "";
  const d = new Date(dateStr + "T00:00:00");
  if (isNaN(d)) return "";
  const days = Math.floor((Date.now() - d.getTime()) / 86400000);
  if (days < 0) return t("scheduled");
  if (days === 0) return t("today");
  if (days === 1) return t("yesterday");
  if (days < 7) return tf("daysAgo", days);
  if (days < 14) return t("lastWeek");
  if (days < 31) return tf("weeksAgo", Math.floor(days / 7));
  if (days < 61) return t("lastMonth");
  if (days < 365) return tf("monthsAgo", Math.floor(days / 30));
  const y = Math.floor(days / 365);
  return y === 1 ? t("lastYear") : tf("yearsAgo", y);
}

// Infer the modality of a release from its text (we don't store it). First match wins.
const MODALITY = [
  { key: "video", icon: "🎬", label: "video", re: /\b(video|sora|veo|runway|kling|movie|text-to-video)\b/i },
  { key: "image", icon: "🖼️", label: "image", re: /\b(image|diffusion|dall-?e|imagen|midjourney|photo|vision|inpaint|text-to-image)\b/i },
  { key: "audio", icon: "🔊", label: "audio", re: /\b(audio|speech|voice|tts|text-to-speech|music|whisper|sound|transcrib)\b/i },
  { key: "robotics", icon: "🤖", label: "robotics", re: /\b(robot|embodied|humanoid|manipulation)\b/i },
  { key: "code", icon: "💻", label: "code", re: /\b(code|coding|copilot|swe-?bench|developer|programming|autocomplete)\b/i },
];
function inferModality(r) {
  const hay = `${r.product} ${r.title} ${r.summary} ${(r.tags || []).join(" ")}`;
  for (const m of MODALITY) if (m.re.test(hay)) return m;
  return { key: "text", icon: "💬", label: "text" };
}

function monthLabel(mk) {
  const [y, m] = (mk || "").split("-");
  return m ? `${t("months")[+m - 1]} ${y}` : "—";
}

const state = { all: [], companies: [], types: [], company: "All", type: "All", access: "All", q: "", lang: "en" };

async function load() {
  try {
    const res = await fetch("releases.json", { cache: "no-store" });
    const data = await res.json();
    state.all = data.releases || [];
    state.companies = data.companies || [];
    state.types = data.types || [];
    state.generatedAt = data.generated_at || "";
    state.lang = localStorage.getItem("radar-lang") || "en";
    readUrl(); // may override lang via ?lang=
    if (!I18N[state.lang]) state.lang = "en";
    document.getElementById("search").value = state.q;
    initLangPicker();
    try {
      const dres = await fetch("digest.json", { cache: "no-store" });
      if (dres.ok) digestData = await dres.json();
    } catch { /* no digest yet — section stays hidden */ }
    await loadContentTranslations();
    applyLang();
    initAnalytics();
    initPalette();
  } catch (err) {
    document.getElementById("meta").textContent = "Could not load releases.json — run `radar build` first.";
    console.error(err);
  }
}

// Fetch content translations for the current language (fail-soft: English stays).
async function loadContentTranslations() {
  i18nMap = {};
  if (state.lang === "en") return;
  try {
    const res = await fetch(`i18n/${state.lang}.json`, { cache: "no-store" });
    if (res.ok) i18nMap = await res.json();
  } catch { /* keep English */ }
}

// Re-render every localized surface (initial paint + language switches).
