// Progressive disclosure: collapse the company wall past this many chips ("+N more").
const COLLAPSE_AT = 12;
let chipsExpanded = false;

function renderChips(containerId, values, key) {
  const el = document.getElementById(containerId);
  el.innerHTML = "";
  let vals = values;
  let hidden = 0;
  if (key === "company" && !chipsExpanded && values.length > COLLAPSE_AT) {
    const visible = values.slice(0, COLLAPSE_AT);
    if (state.company !== "All" && !visible.includes(state.company)) {
      chipsExpanded = true; // the active company must stay visible
    } else {
      hidden = values.length - visible.length;
      vals = visible;
    }
  }
  for (const v of vals) {
    const chip = document.createElement("button");
    chip.className = "chip" + (state[key] === v ? " active" : "");
    const label = v === "All" ? t("all") : key === "type" ? v.toUpperCase() : v;
    chip.innerHTML = `${esc(label)} <span class="cc">${countFor(key, v)}</span>`;
    chip.onclick = () => {
      state[key] = v;
      [...el.children].forEach((c) => c.classList.remove("active"));
      chip.classList.add("active");
      render();
    };
    el.appendChild(chip);
  }
  if (key === "company" && values.length > COLLAPSE_AT) {
    const toggle = document.createElement("button");
    toggle.className = "chip chip-more";
    toggle.textContent = hidden > 0 ? tf("moreChips", hidden) : t("lessChips");
    toggle.onclick = () => {
      chipsExpanded = hidden > 0;
      renderChips(containerId, values, key);
    };
    el.appendChild(toggle);
  }
}

function matches(r) {
  if (state.company !== "All" && r.company !== state.company) return false;
  if (state.type !== "All" && r.type !== state.type) return false;
  if (state.access === "open" && !r.open_source) return false;
  if (state.access === "proprietary" && r.open_source) return false;
  if (state.q) {
    const tr = i18nMap[keyFor(r)];
    const hay = `${r.company} ${r.product} ${r.title} ${r.summary} ${(r.tags || []).join(" ")} ${tr ? tr.title + " " + tr.summary : ""}`.toLowerCase();
    if (!hay.includes(state.q)) return false;
  }
  return true;
}

// Shareable filter links: keep ?company=&type=&access=&q= in sync with state.
function readUrl() {
  const p = new URLSearchParams(location.search);
  for (const k of ["company", "type", "access", "lang"]) if (p.get(k)) state[k] = p.get(k);
  if (p.get("q")) state.q = p.get("q");
}
function syncUrl() {
  const p = new URLSearchParams();
  if (state.company !== "All") p.set("company", state.company);
  if (state.type !== "All") p.set("type", state.type);
  if (state.access !== "All") p.set("access", state.access);
  if (state.q) p.set("q", state.q);
  if (state.lang !== "en") p.set("lang", state.lang);
  const qs = p.toString();
  history.replaceState(null, "", qs ? "?" + qs : location.pathname);
}

